#include "drake/common/memory_buffer_utils.h"

#include <string>
#include "gtest/gtest.h"

using std::string;

namespace drake {
namespace {

GTEST_TEST(MemoryBufferUtilsTest, BasicTest) {
  const int kNumValues = 100;

  // Instantiates and initializes a buffer of bytes.
  uint8_t* buffer = new uint8_t[kNumValues];
  for (int ii = 0; ii < kNumValues; ++ii) {
    buffer[ii] = static_cast<unsigned>(ii);
  }

  // Obtains the string representation of the buffer.
  string buffer_string = MemoryBufferToString(buffer, kNumValues);

  // Defines the expected string representation.
  const string expected_buffer_string =
      "0x00 0x01 0x02 0x03 0x04 0x05 0x06 0x07 0x08 0x09\n"
      "0x0A 0x0B 0x0C 0x0D 0x0E 0x0F 0x10 0x11 0x12 0x13\n"
      "0x14 0x15 0x16 0x17 0x18 0x19 0x1A 0x1B 0x1C 0x1D\n"
      "0x1E 0x1F 0x20 0x21 0x22 0x23 0x24 0x25 0x26 0x27\n"
      "0x28 0x29 0x2A 0x2B 0x2C 0x2D 0x2E 0x2F 0x30 0x31\n"
      "0x32 0x33 0x34 0x35 0x36 0x37 0x38 0x39 0x3A 0x3B\n"
      "0x3C 0x3D 0x3E 0x3F 0x40 0x41 0x42 0x43 0x44 0x45\n"
      "0x46 0x47 0x48 0x49 0x4A 0x4B 0x4C 0x4D 0x4E 0x4F\n"
      "0x50 0x51 0x52 0x53 0x54 0x55 0x56 0x57 0x58 0x59\n"
      "0x5A 0x5B 0x5C 0x5D 0x5E 0x5F 0x60 0x61 0x62 0x63\n";

  // Verifies that the actual and expected string representations of the
  // buffer match.
  EXPECT_EQ(buffer_string, expected_buffer_string);
}

}  // namespace
}  // namespace drake
